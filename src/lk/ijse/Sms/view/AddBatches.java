    private void txtRfreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRfreshActionPerformed
        try {
            List<Batches> SubjectList= new BatchController().getAllBatches();
            DefaultTableModel modal=(DefaultTableModel) jtblBatch.getModel();
            modal.setRowCount(0);
            for(Batches sub:SubjectList){
                Object[] rowData={
                    sub.getBid(),
                    sub.getCid(),
                    sub.getBatch_num()};
                modal.addRow(rowData);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddSubjects.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddSubjects.class.getName()).log(Level.SEVERE, null, ex);
        }
    

    }//GEN-LAST:event_txtRfreshActionPerformed


