    private void loadAllBatches() {
        try {
            List<Batches> BatchList= new BatchController().getAllBatches();
            DefaultTableModel modal=(DefaultTableModel)jtblBatch.getModel();
            for(Batches tempBatch:BatchList){
                
                modal.addRow(new Object[]{
                    
                    tempBatch.getBid(),
                    tempBatch.getCid(),
                    tempBatch.getBatch_num(),
                    
                    
                
                });
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddBatches.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddBatches.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
