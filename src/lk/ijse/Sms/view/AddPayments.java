    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      
        try {
            String pid=txtPid.getText();
            String rid=cmbRid.getSelectedItem().toString();
            String pdate=txtDate.getText();
            String total_amount=txtTotalAmount.getText();
            String paying_amount=txtPayingAmount.getText();
            String remaining_payment=txtRemainingAmount.getText();
            String detail=txtDetails.getText();

            Payments p1=new Payments(pid, rid, pdate, total_amount, paying_amount, remaining_payment, detail);
           
            boolean isSaved=new PaymentController().savePayment(p1);
            if(isSaved){
                JOptionPane.showMessageDialog(null,"Saved");
            }else{
                JOptionPane.showMessageDialog(null,"Try Again","Error",JOptionPane.ERROR_MESSAGE);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddPayments.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddPayments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void txtPidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPidActionPerformed
        if(txtPid.getText().trim().length()>0){
            try {
              
                Payments OBj;
                OBj = new PaymentController().getPayment(txtPid.getText().trim());

                if(OBj!=null){
                     
                     cmbRid.setSelectedItem(OBj.getRid());
                     txtDate.setText(OBj.getPdate());
                     txtTotalAmount.setText(OBj.getTotal_amount());
                     txtPayingAmount.setText(OBj.getPaying_amount());
                     txtRemainingAmount.setText(OBj.getRemaining_payment());
                     txtDetails.setText(OBj.getDetail());

                }else{
                    JOptionPane.showMessageDialog(this,"No Results Fount","Warning",JOptionPane.WARNING_MESSAGE);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(StudentManage.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(StudentManage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Try Again","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtPidActionPerformed

