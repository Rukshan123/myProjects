    private void txtRfreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRfreshActionPerformed
        try {
            List<Payments> SubjectList= new PaymentController().getAllPayments();
            DefaultTableModel modal=(DefaultTableModel) jtblPayment.getModel();
            modal.setRowCount(0);
            for(Payments sub:SubjectList){
                Object[] rowData={
                    sub.getPid(),
                    sub.getRid(),
                    sub.getPdate(),
                    sub.getTotal_amount(),
                    sub.getPaying_amount(),
                    sub.getRemaining_payment(),
                    sub.getDetail()};
                modal.addRow(rowData);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddSubjects.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddSubjects.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_txtRfreshActionPerformed

