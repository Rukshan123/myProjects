    private void txtSidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSidActionPerformed

        if(txtSid.getText().trim().length()>0){
            try {
                Student OBj=new StudentController().getStudent(txtSid.getText().trim());

                if(OBj!=null){
                    txtFname.setText(OBj.getFname());
                    txtLname.setText(OBj.getLname());
                    txtAddress.setText(OBj.getAddress());
                    txtNic.setText(OBj.getNic());
                    txtTp.setText(OBj.getContact_num());
                    txtBirthday.setText(OBj.getDob());
                    cmbGender.setSelectedItem(OBj.getGender());

                }else{
                    JOptionPane.showMessageDialog(this,"No Results Fount","Warning",JOptionPane.WARNING_MESSAGE);
                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(StudentManage.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(StudentManage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Try Again","Error",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_txtSidActionPerformed

