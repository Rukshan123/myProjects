    private void loadAllStudent() {
       
            
        try {
            List<Student> StudentList=new TotalStudentController().getAllStudents();
            DefaultTableModel modal=(DefaultTableModel)jTable1.getModel();
            
            for(Student tempStudent:StudentList){
                
                modal.addRow(new Object[]{
                
                    tempStudent.getSid(),
                    tempStudent.getFname(),
                    tempStudent.getLname(),
                    tempStudent.getAddress(),
                    tempStudent.getNic(),
                    tempStudent.getContact_num(),
                    tempStudent.getDob(),
                    tempStudent.getGender(),
                
                });
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentManage.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StudentManage.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
    }
   
